# list format
a = ["a", 2, 3.4]
print(type(a))
# empty list
a = []
print(type(a))

# tuple format
b = (1, 2, 4, "my")
print(type(b))

# empty tuple
b = ()
print(type(b))

# dict format
d = {
    "name": "shuvo",
    3: 343
}
print(type(d))

# empty dict
d = {}
print(type(d))

# set format
c = {1, 2, 4, "asdf"}
print(type(c))

# empty set
c = set()
print(type(c))
